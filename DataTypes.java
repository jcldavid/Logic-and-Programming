public class DataTypes
{
	public static void main (String[] args)
	{
		char c = 'C';
		int i = 97;
		double d = 0.00;
		byte b = 0;
		i = c;
		d = c;
		System.out.println("c = " + c);
		System.out.println("i = " + i);
		System.out.println("d = " + d);
		System.out.println("b = " + b);
	}
}